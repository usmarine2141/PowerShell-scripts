
Import-LocalizedData -BindingVariable ScriptVariable

Write-DiagProgress -Activity $ScriptVariable.ID_CTSSMB2ServerDriverStateCheck -Status $ScriptVariable.ID_CTSSMB2ServerDriverStateCheckDescription

$RootCauseName = "RC_SMB2ServerDriverStateCheck"

$RootCauseDetected=$false

$var = $null

if (test-path -path "hklm:\System\CurrentControlSet\services\srv2")
{
	$var = get-wmiobject -query "select * from win32_systemdriver where name = 'srv2'"

	#checking for the SMB2 registry value
	if (test-path -path "HKLM:\SYSTEM\CurrentControlSet\services\LanManServer")
	{
	  $checkSmb2RegValue = (Get-ItemProperty -Path "HKLM:\SYSTEM\CurrentControlSet\services\LanManServer\Parameters").SMB2
	}
	
	#checking root cause detection
	if (($var.State -ne "Running") -or ($checkSmb2RegValue -eq 0))
	{ 
		$RootCauseDetected=$true
	}
}

$var = $null

if ($RootCauseDetected)
{
	Update-DiagRootCause -id $RootCauseName -Detected $true
	Write-GenericMessage -RootCauseId $RootCauseName -PublicContentURL "http://support.microsoft.com/kb/2696547" -Verbosity "Warning" -Visibility 4 -SupportTopicsID 8041 -Component "Networking"	
} else
{
	Update-DiagRootCause -id $RootCauseName -Detected $false
}

